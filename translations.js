function t(path) {
  const keys = path.split('.');
  let value = translations[currentLanguage];
  
  for (const key of keys) {
    if (value && typeof value === 'object') {
      value = value[key];
    } else {
      console.warn(`Translation not found: ${path} (${currentLanguage})`);
      return path;
    }
  }
  
  return value || path;
}

